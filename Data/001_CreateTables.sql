-- Secrets
CREATE TABLE KV_SECRETS (
    ID              NUMBER          GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NAME            VARCHAR2(200)   NOT NULL,
    DESCRIPTION     VARCHAR2(1000),
    TYPE            VARCHAR2(30)    NOT NULL,
    ENCRYPTED_VALUE CLOB            NOT NULL,
    PROTECTED_KEY   CLOB            NOT NULL,
    CREATED_BY      VARCHAR2(200)   NOT NULL,
    CREATED_AT      TIMESTAMP       NOT NULL,
    UPDATED_AT      TIMESTAMP       NOT NULL,
    IS_ACTIVE       NUMBER(1)       DEFAULT 1 NOT NULL
);
CREATE UNIQUE INDEX UQ_KV_SECRETS_NAME ON KV_SECRETS(NAME);

-- Grants
CREATE TABLE KV_GRANTS (
    ID              NUMBER          GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    SECRET_ID       NUMBER          NOT NULL,
    PRINCIPAL_TYPE  VARCHAR2(30)    NOT NULL,
    PRINCIPAL_VALUE VARCHAR2(300)   NOT NULL,
    CAN_READ        NUMBER(1)       DEFAULT 1 NOT NULL,
    CAN_ADMIN       NUMBER(1)       DEFAULT 0 NOT NULL,
    GRANTED_BY      VARCHAR2(200)   NOT NULL,
    GRANTED_AT      TIMESTAMP       NOT NULL,
    CONSTRAINT FK_GRANTS_SECRET FOREIGN KEY (SECRET_ID) REFERENCES KV_SECRETS(ID) ON DELETE CASCADE
);
CREATE UNIQUE INDEX UQ_KV_GRANTS ON KV_GRANTS(SECRET_ID, PRINCIPAL_TYPE, PRINCIPAL_VALUE);

-- API Keys
CREATE TABLE KV_API_KEYS (
    ID              NUMBER          GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    LABEL           VARCHAR2(200)   NOT NULL,
    KEY_HASH        VARCHAR2(100)   NOT NULL,
    CREATED_BY      VARCHAR2(200)   NOT NULL,
    CREATED_AT      TIMESTAMP       NOT NULL,
    LAST_USED       TIMESTAMP,
    IS_ACTIVE       NUMBER(1)       DEFAULT 1 NOT NULL
);
CREATE UNIQUE INDEX UQ_KV_API_KEYS_HASH ON KV_API_KEYS(KEY_HASH);

-- Audit Log
CREATE TABLE KV_AUDIT_LOG (
    ID              NUMBER          GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    SECRET_ID       NUMBER,
    PRINCIPAL       VARCHAR2(300)   NOT NULL,
    ACTION          VARCHAR2(50)    NOT NULL,
    DETAIL          VARCHAR2(2000),
    IP_ADDRESS      VARCHAR2(50),
    TIMESTAMP       TIMESTAMP       NOT NULL,
    CONSTRAINT FK_AUDIT_SECRET FOREIGN KEY (SECRET_ID) REFERENCES KV_SECRETS(ID) ON DELETE SET NULL
);
CREATE INDEX IX_KV_AUDIT_SECRET ON KV_AUDIT_LOG(SECRET_ID);
CREATE INDEX IX_KV_AUDIT_TS     ON KV_AUDIT_LOG(TIMESTAMP DESC);
